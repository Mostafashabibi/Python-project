def EX1_4(index):
    '''
return Day of the week
'''
    if index == 1:
        return "Monday"
    elif index == 2:
        return "Tuesday"
    elif index == 3:
        return "Wednesday"
    elif index == 4:
        return "Thursday"
    elif index == 5:
        return "Friday"
    elif index == 6:
        return "Satuarday"
    elif index == 7:
        return "Sunday"


